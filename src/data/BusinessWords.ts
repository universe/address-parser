export const BusinessAbbrMapping: {[key: string]: string } = {};

for (const abbr of Object.keys(BusinessAbbrs) as (keyof typeof BusinessAbbrs)[]) {
  BusinessAbbrMapping[normalize(abbr)] = abbr;
  for (const val of BusinessAbbrs[abbr]) {
    BusinessAbbrMapping[normalize(val)] = abbr;
  }
}

export function toBusinessAbbr(val: string): string {
  return BusinessAbbrMapping[normalize(val)] || val;
}

export function businessAbbrString(val: string): string {
  val = toBusinessAbbr(val);
  const expanded = (BusinessAbbrMapping[normalize(toBusinessAbbr(val))] || [])[0];
  return expanded ? titleCase(expanded) : val;
}

export function isBusinessAbbr(val: string): boolean {
  return !!BusinessAbbrMapping[normalize(val)];
}
